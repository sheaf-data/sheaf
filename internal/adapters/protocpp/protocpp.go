// Package protocpp implements a test-parser adapter for C++ test
// suites whose contract surface is declared in protobuf. Like gtest,
// it walks *_test.cc files for TEST(), TEST_F(), TEST_P() (and
// project-supplied extra macros), but it extracts ContractRefs using
// the idioms protoc's C++ bindings produce — patterns common to envoy,
// grpc, kubernetes, and any other gRPC-shaped C++ codebase. The
// adapter was originally developed to scan envoy's xDS test tree but
// is parameterized by IDLPrefix so it works against any protobuf
// project.
//
// Patterns recognized:
//
//   - String-literal gRPC method-descriptor names:
//     "<prefix>.<pkg>.<Service>.<Method>"
//     gRPC clients look up RPCs by their fully-qualified method name,
//     passed as a quoted string to DescriptorPool::FindMethodByName.
//     These are the only direct textual reference to specific RPCs in
//     C++ test code that uses mocks instead of generated stubs.
//
//   - Fully-qualified C++ proto type references:
//     <prefix>::<pkg>::<TypeName>
//     Generated by protoc from `package <prefix>.<pkg>;`. The bare
//     local name AND the dotted form are both emitted as refs,
//     matching the aliases the proto adapter attaches to TYPE
//     elements.
//
//   - Generated *.pb.h include scope:
//     #include "<prefix>/<pkg>/<basename>.pb.h"
//     Adds the implied package to the file's library scope so
//     unqualified `<TypeName>` references resolve to the right library
//     when only one is in scope.
//
//   - `*_test_harness.h` include following:
//     Test harness headers carry the fixture's descriptor wiring (the
//     FindMethodByName call) used by every test in the .cc; we scan
//     the harness header and propagate its refs to all tests in the
//     including file.
//
//   - Per-test body windows are brace-counted (skipping strings,
//     chars, and // / /* */ comments) so a free-standing helper class
//     defined between two TEST() macros doesn't leak its refs into
//     the surrounding tests.
//
//   - Preamble named-constants (`constexpr char NAME[] = "...";`) only
//     propagate their refs to tests that mention NAME in their body —
//     constants used by some tests don't false-positive every other
//     test in the file.
//
//   - When the adapter finds confident direct-ref evidence on a test
//     (qualified-type or string-literal or harness-derived), it
//     suppresses NameTokens so the indexer's fuzzy name-token
//     attribution (Strategy 2) doesn't add false positives on top.
//
//   - Namespace-scope-aware test-fixture references:
//     namespace <prefix>::<pkg> { TEST(Fixture, …) { ... } }
//     For every TEST / TEST_F / TEST_P / TYPED_TEST / TYPED_TEST_P
//     macro lexically enclosed by a C++ namespace whose leading
//     segment matches idl_prefix, the macro's first argument (the
//     fixture name) is emitted as a bare candidate ref. Pigweed is
//     the canonical case (`namespace pw::rpc { TEST(EchoService, …) }`);
//     parts of gRPC and envoy follow the same convention. The
//     indexer's existing alias-match path joins those bare refs to
//     the right contract element via the short-form alias the proto
//     adapter already emits.
//
// The new patterns are additive — none of the existing fuchsia/grpc
// gtest behavior is affected, because protocpp is a distinct adapter
// selected by name in the textproto.
